try:
    # Ввод общего количества мандаринов (K) и числа учеников (N)
    K = int(input("общее количество мандаринов  "))
    N = int(input("количество учеников  "))
    # Расчет количества целых мандаринов, которое получит каждый ученик
    каждый_ученик_получит = K // N
    # Расчет количества целых мандаринов, которые останутся в корзине
    мандарины_в_корзине = K % N
    # Вывод результатов с использованием форматированного вывода (f-строк)
    print(f"Каждый ученик получит {каждый_ученик_получит} целых мандаринов.")
    print(f"{мандарины_в_корзине} целых мандаринов останется в корзине.")
except ValueError:
    # Обработка исключения, если введены нецелые числа
    print("Ошибка: Введите целые числа для K и N.")
except ZeroDivisionError:
    # Обработка исключения, если N равно нулю (деление на ноль)
    print("Ошибка: Количество учеников (N) не может быть нулевым.")